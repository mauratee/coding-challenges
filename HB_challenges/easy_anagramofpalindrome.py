"""Is the word an anagram of a palindrome?

A palindrome is a word that reads the same forward and backwards
(eg, "racecar", "tacocat"). An anagram is a rescrambling of a word
(eg for "racecar", you could rescramble this as "arceace").

Determine if the given word is a re-scrambling of a palindrome.
The word will only contain lowercase letters, a-z.

Examples::

    >>> is_anagram_of_palindrome("a")
    True

    >>> is_anagram_of_palindrome("ab")
    False

    >>> is_anagram_of_palindrome("aab")
    True

    >>> is_anagram_of_palindrome("arceace")
    True

    >>> is_anagram_of_palindrome("arceaceb")
    False

"""


def is_anagram_of_palindrome(word):
    """Is the word an anagram of a palindrome?"""

    # create list
    odd_characters = []

    # track characters that appear an odd number of times:
    for char in word:
    # if char in word in list, remove char from list
        if char in odd_characters:
            odd_characters.remove(char)
        # if char not in list, add to list
        else:
            odd_characters.append(char)

    # return is length of list less than or equal to 1?
    return len(odd_characters) <= 1


